package com.example.demo;

public class CustomEntry {
    private String xValue;
    private float yValue;

    public CustomEntry(String xValue, float yValue) {
        this.xValue = xValue;
        this.yValue = yValue;
    }

    public String getXValue() {
        return xValue;
    }

    public float getYValue() {
        return yValue;
    }
}